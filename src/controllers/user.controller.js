// ─── Helpers ─────────────────────────────────────────────────────────────────

const pick = (arr) => arr[Math.floor(Math.random() * arr.length)];

const format = (template, vars) =>
  template.replace(/\{(\w+)\}/g, (_, key) => vars[key] ?? `{${key}}`);

/** الوقت من اليوم بالعربي */
const getTimePeriod = () => {
  const hour = new Date().getHours();
  if (hour >= 5  && hour < 12) return 'الصبح';
  if (hour >= 12 && hour < 15) return 'الضهر';
  if (hour >= 15 && hour < 19) return 'العصر';
  if (hour >= 19 && hour < 22) return 'المغرب';
  return 'الليل';
};

// ─── Controller ───────────────────────────────────────────────────────────────

// @desc    Get dynamic smart greeting based on weather details (Arabic)
// @route   GET /api/user/greeting
// @access  Private
const getGreeting = async (req, res) => {
  const user = await User.findById(req.user._id);
  if (!user) return res.status(404).json({ success: false, message: 'User not found' });

  const { username, governorate } = user;
  const firstName = username.trim().split(' ')[0];  // ناخد أول كلمة من اسم المستخدم
  const govArabic = governoratesArabic[governorate] || governorate;

  try {
    const weatherData = await getCurrentWeather(governorate);

    // ── سحب كل تفاصيل الـ API ──────────────────────────────────────────────
    const temp       = Math.round(weatherData.main.temp);
    const feelsLike  = Math.round(weatherData.main.feels_like);
    const humidity   = weatherData.main.humidity;           // %
    const windSpeed  = weatherData.wind.speed;              // m/s
    const windKmh    = Math.round(windSpeed * 3.6);         // km/h (أوضح للقارئ)
    const pressure   = weatherData.main.pressure;           // hPa
    const clouds     = weatherData.clouds?.all ?? 0;        // %
    const conditionId = weatherData.weather[0].id;          // OpenWeather condition code
    const condition  = weatherData.weather[0].main.toLowerCase();
    const timePeriod = getTimePeriod();

    // متغيرات الـ templates
    const vars = {
      name:      firstName,   // الاسم الأول بس
      gov:       govArabic,
      temp,
      feelsLike,
      hum:       humidity,
      wind:      windKmh,
      clouds,
      pressure,
      time:      timePeriod,
    };

    let templates = [];

    if (conditionId >= 200 && conditionId < 300) {
      templates = [
        `⚡ رعد وبرق في {gov} يا {name}! خلي الناس بعيد عن الحقول والأشجار العالية دلوقتي. الأولوية للسلامة مش الزراعة.`,
        `⛈️ عواصف رعدية في {gov} يا {name}! أمّن المعدات والزرع المكشوف فوراً، وابعد نفسك وعمالتك عن الأماكن المفتوحة.`,
        `🌩️ يا {name}، إنذار طقس في {gov}! تأجيل أي شغل في الهواء الطلق ضروري دلوقتي. تأكد إن الصوب والمخازن مقفولة كويس.`,
        `⚡ عواصف كهربائية في {gov} يا {name}! ده مش وقت المزرعة، ده وقت تحمي نفسك وعمالتك. السلامة أولاً وأخيراً.`,
      ];
    }

    else if ([731, 751, 761, 762].includes(conditionId)) {
      templates = [
        `🌪️ عاصفة رملية في {gov} يا {name}! غطّي النباتات الحساسة والبذور فوراً. الزرع الصغير هيحتاج حماية من الرمل والتراب.`,
        `🏜️ رياح الخماسين في {gov} يا {name}! تجنب الري النهاردة، التربة الجافة المتطايرة هتضر الزرع أكتر من النفع. غطّي مصادر المياه.`,
        `💨 عاصفة تراب في {gov}! يا {name}، أغلق فتحات الصوب الزراعي كويس. الجو ده بيضر بالتنفس وبيعيق التلقيح الطبيعي.`,
        `🌫️ خماسين في {gov} يا {name}! علّق أي رش أو معالجة كيميائية النهاردة وريّح الحيوانات في الحظيرة. لو تقدر غطّي الثمار.`,
        `🌪️ يا {name}، تراب وهواء في {gov}! الأشجار الصغيرة في خطر اتساقط. دعّمها بأعواد وتجنب الشغل في الهواء الطلق.`,
      ];
    }

    else if (condition.includes('rain') && (conditionId >= 502 || (conditionId >= 312 && conditionId <= 321))) {
      templates = [
        `🌧️ مطر غزير في {gov} يا {name}! راقب تصريف التربة فوراً، الماء الزيادة يتلف الجذور ويسبب العفن في وقت قصير.`,
        `⛈️ أمطار شديدة في {gov}! يا {name}، افحص قنوات الصرف وتأكد إنها شغالة. الغرق هنا أخطر من الجفاف بكتير.`,
        `🌊 مطر قوي في {gov} يا {name}. وقت ممتاز تعمل فيه جرد للمزرعة من جوه وتخطط للأسبوع الجاي بدل ما تتبلل في الغيط.`,
        `💧 أمطار غزيرة في {gov}! يا {name}، راقب الزرع الصغير من إنه ميتغطيش بالطمي. وريّح من الري طبعاً، المطر بيعمل الشغل.`,
        `🌧️ يا {name}، المطر الشديد في {gov} ممكن يضغط على التربة الطينية. لو الحقل منحدر شوية، تأكد إن التربة مش بتنجرف.`,
      ];
    }

    // ── 4. مطر خفيف / معتدل ──────────────────────────────────────────────────
    else if (condition.includes('rain') || (conditionId >= 300 && conditionId < 600)) {
      templates = [
        `🌦️ مطر خفيف في {gov} يا {name}! ريّة ربانية للزرع، وريّح نفسك من الشلنج النهاردة. يوم استراحة واستعداد.`,
        `☔ الجو بيمطر في {gov}! يا {name}، فرصة ذهبية، خلي التربة تشرب كويس وراقب إن الماء موزّع بالتساوي على الحقل.`,
        `🌧️ نعمة المطر في {gov} يا {name}! توفير في فاتورة الري النهاردة. بس افحص إن الصرف تمام عشان الماء ميركدش حوالين الجذور.`,
        `🌱 مطر رائع في {gov} يا {name}! الوقت ده مثالي للشتل إذا الأرض مش طينة أوي. التربة رطبة ومستعدة لتقبل البذور.`,
        `☔ يا {name}، {gov} بتتبرد بالمطر النهاردة! راقب النباتات الحساسة إذا كان الهواء بارد مع المطر، متفاجأش بأمراض بكتيرية.`,
      ];
    }

    // ── 5. ضباب / شبورة ──────────────────────────────────────────────────────
    else if ((conditionId >= 700 && conditionId < 732) || conditionId === 741) {
      templates = [
        `🌫️ ضباب في {gov} يا {name}! الجو ده بيرفع رطوبة الأوراق جداً، خلي بالك من الأمراض الفطرية وراقب الزرع كويس.`,
        `😶‍🌫️ شبورة في {gov} يا {name}! ابعد عن الرش لحد ما الجو يصفى. المبيد مش هينتشر صح في الضباب ده.`,
        `🌁 ضباب صباحي في {gov}! يا {name}، مش وقت رش مبيدات دلوقتي. استنى بعد {time} شوية الجو يصحى.`,
        `🌫️ الشبورة موجودة في {gov} يا {name}. الهواء رطب كده بيساعد انتشار بكتيريا وفطر على الأوراق. راقب كويس.`,
        `🌁 يا {name}، الضباب في {gov} بيخلي الرؤية ضعيفة والرطوبة عالية. يوم مناسب للشغل الداخلي في المخزن أو الإدارة.`,
      ];
    }

    // ── 6. رياح عاتية جداً (> 14 m/s ≈ 50 km/h) ────────────────────────────
    else if (windSpeed > 14) {
      templates = [
        `💨 رياح عاتية ({wind} كم/س) في {gov} يا {name}! أمّن الصوب الزراعية وربط كل إيه ممكن يتطير فوراً. خطر حقيقي على الزرع.`,
        `🌬️ عواصف هوائية شديدة في {gov}! يا {name}، الأشجار الضعيفة والزرع العالي في خطر اتكسر. دعّم اللي تقدر دلوقتي.`,
        `💨 رياح جداً قوية ({wind} كم/س) يا {name}! وقف أي رش فوراً، الرش هيضيع في الهواء. ريّح الأجهزة وأمّن المعدات.`,
        `🌪️ يا {name}، {wind} كم/س في {gov} ده خطر! الريحة دي ممكن تقلع الغطاء البلاستيك عن الصوب وتكسر الأغصان. طوارئ.`,
      ];
    }

    // ── 7. رياح قوية (8-14 m/s ≈ 29-50 km/h) ───────────────────────────────
    else if (windSpeed > 8) {
      templates = [
        `💨 هوا نشيط ({wind} كم/س) في {gov} يا {name}! تجنب رش المبيدات والأسمدة السائلة عشان ميطيروش. بلاش تضيّع فلوسك.`,
        `🌬️ رياح معتدلة الشدة في {gov} يا {name}. فرصة تهوية جيدة للصوب الزراعية المغلقة، بس ابعد عن الرش النهاردة.`,
        `💨 جو عليه هوا ({wind} كم/س) في {gov} يا {name}! لو عندك شتلات صغيرة مشتولة حديثاً، تأكد إنها مدعومة ومش هتتكسر.`,
        `🌿 رياح في {gov} يا {name}. الهوا النهاردة بيساعد تجفيف الرطوبة الزيادة من الأوراق، بس احسب إن البخر من التربة هيزيد.`,
        `💨 يا {name}، الهوا في {gov} قوي شوية ({wind} كم/س). راقب الزرع العالي زي الذرة والقصب، وادعم اللي بان عليه ترنح.`,
      ];
    }

    // ── 8. حر شديد + رطوبة عالية (الأخطر زراعياً) ──────────────────────────
    else if (temp >= 30 && humidity >= 75) {
      templates = [
        `🥵💧 يا {name}، {gov} عندها حر ({temp}°) مع رطوبة عالية ({hum}%)! ده الجو المثالي للفطريات والعفن. افحص الأوراق كويس جداً.`,
        `🌡️💦 حر ورطوبة في {gov}! يا {name}، زرعك في خطر مزدوج: إجهاد حراري + أمراض فطرية. قلل ري السطح وزوّد التهوية بين النباتات.`,
        `⚠️ يا {name}، {temp}° مع {hum}% رطوبة في {gov} = دفيئة طبيعية للبكتيريا والفطر! فكر في رش فطريسيد وقائي اليومين دول.`,
        `🌡️ تحذير زراعي في {gov}! حر + رطوبة عالية يا {name}. راقب أوراق الطماطم والخيار والبطاطس من البياض الزغبي والعفن الرمادي.`,
        `💧🔥 يا {name}، مناخ {gov} النهاردة صعب: {temp}° درجة و{hum}% رطوبة. الريّة السطحية ممنوعة، روي من تحت الأرض لو تقدر.`,
      ];
    }

    // ── 9. حر قياسي (>= 38°C) ────────────────────────────────────────────────
    else if (temp >= 38) {
      templates = [
        `🔥 موجة حر شديدة ({temp}°) في {gov} يا {name}! روي بدري جداً قبل الشمس. في الحر ده الماء بيتبخر بسرعة كبيرة جداً.`,
        `☀️ درجات قياسية ({temp}°) في {gov}! يا {name}، غطّي التربة بالمالش لحبس الرطوبة. النباتات الحساسة محتاجة ظل مؤقت.`,
        `🌡️ حر لا يُطاق ({temp}° / حسّ بالجسم {feelsLike}°) في {gov} يا {name}! شغّل الري الليل أو الفجر. النبات في الحر ده بيتعرق أكتر من الامتصاص.`,
        `🥵 يا {name}، الجو وصل {temp}° في {gov}! الزرع الحساس زي الطماطم والخيار ممكن يُصاب بحروق شمس. فكر في ظليل مؤقت عليهم.`,
        `🔆 {temp}° في {gov} يا {name}! إنت وعمالتك لازم تشتغلوا بدري أوي وتريّحوا من الساعة ١١ لـ ٣ عشر كده. صحة الجميع أولاً.`,
        `🌞 يا {name}، موجة الحر في {gov} شديدة! الأرض هتشقق بسرعة في {temp}° دي. راقب الري وزوّد تردده عشان متخسرش الزرع.`,
      ];
    }

    // ── 10. حر معتدل (30-37°C) ───────────────────────────────────────────────
    else if (temp >= 30) {
      templates = [
        `☀️ حر معتدل ({temp}°) في {gov} يا {name}! متنساش تروي بدري الصبح أو بعد الغروب عشان تقلل بخر الماء من التربة.`,
        `🌡️ الجو حامي ({temp}°) في {gov} يا {name}. حافظ على رطوبة التربة وتجنب رش الأوراق في وسط النهار، بيعمل حروق.`,
        `🌞 {temp}° في {gov} يا {name}! الماء بيتبخر أسرع في الجو ده. زوّد تردد الري شوية، وفضّل الري في {time} لو الجو أقل حرارة.`,
        `🌿 يا {name}، {temp}° في {gov} جو مناسب للمحاصيل الحارة زي الفلفل والباذنجان والبطاطس. بس خلي بالك من البخر الزيادة.`,
        `☀️ صيف {gov} شغّال يا {name}! {temp}° بتحتاج ري منتظم. وريّح عمالتك في وسط النهار واديهم ميه كتير عشان ميصحوش.`,
        `🌾 يا {name}، الحرارة {temp}° في {gov} دلوقتي والحس بالجسم {feelsLike}°. متنساش تراقب النباتات الشبابية الجديدة، هي أكتر عرضة للإجهاد.`,
      ];
    }

    // ── 11. صقيع / برد قارس (<= 5°C) ────────────────────────────────────────
    else if (temp <= 5) {
      templates = [
        `❄️ تحذير صقيع في {gov} يا {name}! {temp}° خطر على معظم المحاصيل. غطّي النباتات الحساسة فوراً بأقمشة أو بلاستيك من الليل.`,
        `🥶 برد قارص ({temp}°) في {gov}! يا {name}، جذور النباتات تحتاج عزل. وقف الري خالص، الماء في الجو ده هيجمد ويكسر الخلايا.`,
        `🌨️ درجات قريبة من الصفر في {gov} يا {name}! الليمون والموز والباذنجان في خطر حقيقي. دفّيهم دلوقتي قبل فوات الأوان.`,
        `❄️ يا {name}، {gov} باردة جداً ({temp}°)! تجنب الري الرشاش النهاردة، الماء على الأوراق في البرد ده بيسبب إصابات تجميد مباشرة.`,
        `🌡️ صقيع محتمل في {gov} يا {name}! الجو {temp}° دلوقتي. راقب الزرع الحساس طول الليل ولو تقدر شغّل نظام تدفئة أو أضرم نيران حولهم.`,
      ];
    }

    // ── 12. برد شتوي (6-15°C) ────────────────────────────────────────────────
    else if (temp <= 15) {
      templates = [
        `🧥 جو شتوي بارد ({temp}°) في {gov} يا {name}. قلل الري وراقب درجة حرارة التربة، النباتات بتستهلك ماء أقل في البرد.`,
        `❄️ برودة محببة ({temp}°) في {gov} يا {name}! الجو ده مناسب جداً للمحاصيل الشتوية زي الفراولة والخس والبروكلي والجزر.`,
        `🌡️ {temp}° في {gov} يا {name}. لو عندك أصناف استوائية حساسة زي الريحان والباذنجان، وفرلها غطاء أو نقّلها لمكان أدفى.`,
        `🌿 جو بارد ناعم في {gov} يا {name}! {temp}° بتخلي الخضروات الورقية زي السبانخ والبروكلي يطلعوا بشكل رائع. استغل الوقت.`,
        `🥦 يا {name}، برد {gov} ({temp}°) مناسب للشتل والزرع الشتوي. التربة هتحتفظ بالرطوبة أحسن من الجو الحامي. وقت ذهبي!`,
        `🌱 جو {gov} البارد ({temp}°) بيريّح يا {name}! شغّل طاقتك في تجهيز التربة والتسميد. النباتات الشتوية سعيدة في الجو ده.`,
      ];
    }

    // ── 13. رطوبة عالية جداً (>= 80%) ──────────────────────────────────────
    else if (humidity >= 80) {
      templates = [
        `💧 رطوبة عالية جداً ({hum}%) في {gov} يا {name}! خطر مرتفع جداً للفطريات. افحص الأوراق من تحت وفوق كويس.`,
        `🔍 {hum}% رطوبة في {gov} يا {name}! بيئة مثالية للبياض الزغبي والعفن الرمادي. قلل رطوبة التربة الزيادة وهوّي كويس.`,
        `💦 يا {name}، الهواء رطب جداً في {gov} ({hum}%). تجنب الري فوق الأوراق خالص وخلي التهوية بين النباتات مفتوحة.`,
        `🌿 رطوبة شديدة في {gov} يا {name}! ده وقت تفتكر فيه تعمل رش وقائي بفطريسيد. الرطوبة دي بتساعد انتشار الإصابة بسرعة.`,
        `💧 يا {name}، {hum}% رطوبة في {gov}! قلل معدل الري النهاردة، التربة مش هتبخر بسرعة. وهوّي جيد حوالين الزرع.`,
      ];
    }

    // ── 14. رطوبة عالية (70-79%) ─────────────────────────────────────────────
    else if (humidity >= 70) {
      templates = [
        `💧 رطوبة مرتفعة ({hum}%) في {gov} يا {name}. الجو ده بيحفز الأمراض الفطرية! افحص أوراق زرعك، ابدأ من تحت.`,
        `🔍 {hum}% رطوبة في {gov} يا {name}! خلي بالك من البياض الزغبي واللطعة البكتيرية. قلل ري السطح شوية.`,
        `💦 يا {name}، الرطوبة مرتفعة في {gov} ({hum}%). تجنب رش الأوراق في {time} دلوقتي وخلي التهوية بين النباتات كويسة.`,
        `🌿 رطوبة في {gov} مرتفعة يا {name}! وقت مناسب تراقع فيه الزرع وتفحص إذا في بوادر إصابة. البكري أحسن من العلاج.`,
      ];
    }

    // ── 15. جفاف شديد (<= 20%) ───────────────────────────────────────────────
    else if (humidity <= 20) {
      templates = [
        `🏜️ جفاف شديد جداً ({hum}%) في {gov} يا {name}! الزرع هيعطش بسرعة كبيرة. زوّد الري فوراً وفكر في الرش على الأوراق.`,
        `🌵 هواء جاف للغاية في {gov} يا {name}! {hum}% رطوبة بس. الأوراق هتفقد ماءها بسرعة. راقب ذبول الزرع باستمرار.`,
        `🏜️ يا {name}، الجو في {gov} جاف جداً! {hum}% فقط. النباتات الورقية زي الخس والسبانخ هتتأثر الأول. راقب الأوراق من التجعد.`,
        `💨 رطوبة منخفضة جداً ({hum}%) يا {name}! البخر من التربة في {gov} هيكون عالي جداً. غطي التربة بالمالش وزوّد الري.`,
      ];
    }

    // ── 16. جفاف (21-35%) ────────────────────────────────────────────────────
    else if (humidity <= 35) {
      templates = [
        `🌬️ جو جاف ({hum}%) في {gov} يا {name}. الزرع هيعطش أسرع النهاردة. زوّد معدل الري شوية وراقب الأوراق من أعراض العطش.`,
        `💧 رطوبة منخفضة ({hum}%) في {gov} يا {name}! تأكد إن الري وصل كويس للجذور مش بس السطح. الجفاف السطحي خادع.`,
        `🌵 هواء جاف شوية في {gov} يا {name}. {hum}% رطوبة. راقب كمية الري وتأكد إن التربة فاضلة رطبة كفاية على الأقل ٥ سم.`,
        `🏜️ يا {name}، الجو في {gov} جاف ({hum}%). البخر هيكون أعلى من المعتاد. لو عندك مالش على الأرض، دلوقتي وقت تشيكه.`,
      ];
    }

    // ── 17. غيوم كتير بدون مشاكل تانية (>= 75%) ─────────────────────────
    else if (clouds >= 75) {
      templates = [
        `☁️ غيوم كتير في {gov} يا {name}! جو لطيف للشغل في الهواء الطلق. النباتات بتحب الجو الملبد. يوم إنتاجي!`,
        `🌥️ سما ملبدة بالغيوم في {gov} يا {name}. درجة حرارة مريحة. فرصة تجلس تفحص المزرعة كلها بدون ما الشمس تتعبك.`,
        `☁️ يا {name}، الغيوم بتحمي زرع {gov} من شمس الضهر! وقت ممتاز للشتل أو التقليم أو أي شغل بدني في الحقل.`,
        `🌥️ يا {name}، جو غيوم في {gov} وكويس للزرع! الشمس مش قوية النهاردة، يعني بخر أقل وزرع أسعد.`,
      ];
    }

    // ── 18. طقس مثالي (Default) ──────────────────────────────────────────────
    else {
      templates = [
        `🌱 طقس معتدل ومثالي للزراعة في {gov} النهاردة يا {name}! {temp}° مع {hum}% رطوبة. يوم إنتاجي بامتياز، استغله!`,
        `🌿 جو لطيف ({temp}°) في {gov} يا {name}! ده الوقت المثالي للشتل والتقليم والتسميد. الزرع هيستقبل كل الاهتمام دلوقتي.`,
        `☀️ طقس {gov} رائع النهاردة يا {name}! {temp}° مناسبة جداً للعمل. وقت يناسب كل أنواع الشغل الزراعي، شغّل طاقتك!`,
        `🌾 أحسن يوم للمزرعة في {gov} يا {name}! {temp}° ورطوبة {hum}%. مش هتلاقي جو أحسن من ده، استغل اليوم كله.`,
        `🌻 يا {name}، {gov} عندها جو معتدل وجميل ({temp}°) في {time}! فرصة تعمل كل الأعمال المؤجلة في مزرعتك النهاردة.`,
        `🌱 حرارة {temp}° ورطوبة {hum}% في {gov} يا {name}! الجو ده بيريّح الزرع والفلاح. شغّل بركاتك بكل جهد.`,
      ];
    }

    // ── تجميع الرسالة النهائية ────────────────────────────────────────────────
    const finalMessage = format(pick(templates), vars);

    res.status(200).json({
      success: true,
      data: {
        message: finalMessage,
        weather: {
          temp,
          feelsLike,
          humidity,
          windSpeed,
          windSpeedKmh: windKmh,
          clouds,
          pressure,
          conditionId,
          condition: weatherData.weather[0].main,
          description: weatherData.weather[0].description,
        }
      }
    });

  } catch (error) {
    
    // Fallback مع استخدام الاسم الأول المستخرج من اليوزر نيم
    const fallbacks = [
      `🌱 أهلاً بيك يا ${firstName}! نتمنى تكون إنت وكل زرعك في ${govArabic} بألف خير النهاردة!`,
      `🌿 يا ${firstName}! ربنا يبارك في ${govArabic} وكل فلاحيها وزرعها، يوم مبارك عليك!`,
      `☀️ صباح النور يا ${firstName}! يوم مبارك على مزرعتك في ${govArabic}. شغّل بركاتك!`,
      `🌾 مرحباً يا ${firstName}! اتمنى يومك في ${govArabic} يكون مليان إنتاج وخير.`,
    ];

    res.status(200).json({
      success: true,
      data: {
        message: pick(fallbacks),
        weather: null
      }
    });
  }
};

